function absolutePath(path: string): string {
  const expanded = path.trim().replace(/^~(?=\/|$)/, homedir());
  return isAbsolute(expanded) ? expanded : resolve(projectBaseDir(), expanded);
}

export function parseTelegramAllowedUserIds(
  singleUserId: number | undefined,
  rawUserIds: string | undefined
): number[] {
  const ids = [
    singleUserId,
    ...(rawUserIds
      ? rawUserIds.split(",").map((part) => {
        const trimmed = part.trim();
        if (!/^-?\d+$/.test(trimmed)) {
          throw new Error(`텔레그램 사용자 ID가 정수가 아닙니다: ${part}`);
        }
        return Number(trimmed);
      })
      : [])
  ].filter((id): id is number => id !== undefined);
  if (ids.length === 0) {
    throw new Error("TELEGRAM_ALLOWED_USER_ID 또는 TELEGRAM_ALLOWED_USER_IDS가 필요합니다.");
  }
  return [...new Set(ids)];
}

// 여러 Codex 계정의 CODEX_HOME 디렉터리(CSV)를 파싱·검증한다. 각 홈은 디렉터리여야 하고
// 내부에 auth_mode가 "chatgpt"인 auth.json이 있어야 한다(구독 로그인만 허용). 비어 있으면
// 폴백 홈 1개만 반환해 단일 계정 동작과 하위호환을 유지한다.
export function parseCodexAccountHomes(
  raw: string | undefined,
  fallbackHome: string
): string[] {
  if (raw === undefined || raw.trim() === "") {
    return hasCodexSubscriptionAuth(fallbackHome) ? [fallbackHome] : [];
  }
  const entries = raw.split(",").map((part) => part.trim()).filter(Boolean);
  const seen = new Set<string>();
  const result: string[] = [];
  for (const entry of entries) {
    const absPath = absolutePath(entry);
    if (!existsSync(absPath) || !statSync(absPath).isDirectory()) {
      throw new Error(`Codex 계정 홈이 디렉터리가 아닙니다: ${entry}`);
    }
    const authPath = join(absPath, "auth.json");
    if (!existsSync(authPath)) {
      throw new Error(`Codex 계정 홈에 auth.json이 없습니다: ${entry}`);
    }
    let auth: unknown;
    try {
      auth = JSON.parse(readFileSync(authPath, "utf8")) as unknown;
    } catch {
      throw new Error(`Codex auth.json을 읽을 수 없습니다: ${entry}`);
    }
    if (
      typeof auth !== "object"
      || auth === null
      || (auth as Record<string, unknown>)["auth_mode"] !== "chatgpt"
    ) {
      throw new Error(`Codex 계정은 ChatGPT 구독 로그인이어야 합니다(auth_mode!=chatgpt): ${entry}`);
    }
    // 검증을 통과한 뒤에야 canonical 경로로 중복을 제거한다(존재하지 않는 경로에서 realpath가
    // 먼저 던지지 않도록 순서를 지킨다). 등록 순서는 보존한다.
    const canonical = realpathSync(absPath);
    if (seen.has(canonical)) continue;
    seen.add(canonical);
    result.push(absPath);
  }
  return result.length > 0 ? result : [fallbackHome];
}

function hasCodexSubscriptionAuth(home: string): boolean {
  const authPath = join(home, "auth.json");
  try {
    if (!existsSync(home) || !statSync(home).isDirectory() || !existsSync(authPath)) return false;
    const auth = JSON.parse(readFileSync(authPath, "utf8")) as Record<string, unknown>;
    return auth["auth_mode"] === "chatgpt";
  } catch {
    return false;
  }
}

function hasAgySubscriptionAuth(home: string): boolean {
  const onboardingPath = join(home, ".gemini", "antigravity-cli", "cache", "onboarding.json");
  if (!existsSync(onboardingPath)) return false;
  try {
    const onboarding = JSON.parse(readFileSync(onboardingPath, "utf8")) as Record<string, unknown>;
    return onboarding["onboardingComplete"] === true
      || onboarding["consumerOnboardingComplete"] === true;
  } catch {
    return false;
  }
}

function hasGrokSubscriptionAuth(home: string): boolean {
  const authPath = join(home, ".grok", "auth.json");
  if (!existsSync(authPath)) return false;
  try {
    const auth = JSON.parse(readFileSync(authPath, "utf8")) as Record<string, unknown>;
    return Object.values(auth).some((entry) =>
      !!entry
      && typeof entry === "object"
      && typeof (entry as Record<string, unknown>)["key"] === "string"
      && ((entry as Record<string, unknown>)["key"] as string).trim().length > 0
    );
  } catch {
    return false;
  }
}

export function detectAuthenticatedProviders(options: {
  claudeOauthTokens: readonly string[];
  codexAccountHomes: readonly string[];
  home?: string;
}): ProviderKind[] {
  const home = options.home ?? homedir();
  const providers: ProviderKind[] = [];
  if (options.claudeOauthTokens.length > 0) providers.push("claude");
  if (options.codexAccountHomes.some(hasCodexSubscriptionAuth)) providers.push("codex");
  if (hasAgySubscriptionAuth(home)) providers.push("agy");
  if (hasGrokSubscriptionAuth(home)) providers.push("grok");
  return providers;
}

